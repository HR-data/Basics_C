// switch case break
#include<stdio.h>

int main()
	{
	 int i=5;
	 switch(i)
	 	{
	 	case 0 :
	 		printf("zero \n");
	 		
	 		break;
	 	case 1:
	 		printf("one \n");
	 		
	 		break;
	 	case 2:
	 		printf("two \n");
	 		
	 		break;
	 	default :
	 		printf("not valid number \n");
	 		
	 		break;				
	 	
	 	
	 	
	 	
	 	}
	
	
	
	
	return 0;
	}
